# Array Iteration: Iterating through an array (or list) is a fundamental concept where you go through 
# each element of the array. In Python, this is often done using a for loop.
# Example:

nums = [2, 7, 11, 15]
for number in nums:
    print(number)
    
strings = ["Hello", "World", "!"]
for string in strings:
    print(string)